<script lang="ts">
  import { onMount } from "svelte";

  /**
   * A decorative jet airliner that endlessly lands on and takes off from the
   * top edge of the printer shell. The artwork is an optimized flat-design
   * vector (99 paths, ~22 KB) recolored into the printer's own livery: paper
   * white body, plastic-grey details, and the ON light's orange on the tail.
   * Every fill rides a CSS token, so dark mode restyles it automatically.
   *
   * One full cycle, always travelling left to right: descending approach
   * from off-screen left, flare + touchdown (with a little dust puff),
   * decelerating rollout, slow taxi across the middle, accelerating takeoff
   * roll with a late nose rotation, climb out off-screen right, then a quiet
   * beat with an empty deck before looping.
   */
  let lane: HTMLDivElement;
  let flyer: HTMLDivElement;
  let puffEl: HTMLDivElement;
  let plane: SVGGElement;
  let exhaust: SVGGElement;

  onMount(() => {
    // Phase timeline (ms). Order matters; the loop walks this list.
    const PHASES = [
      { k: "approach", d: 2950 }, // descending glide, off-left -> 8% across
      { k: "flare", d: 1100 }, // nose-up flare, settles onto the wheels
      { k: "rollout", d: 1900 }, // decelerating ground roll
      { k: "taxi", d: 2400 }, // slow taxi across mid-deck
      { k: "roll", d: 2800 }, // accelerating takeoff roll, rotates late
      { k: "climb", d: 2000 }, // lifts off, climbs off-right
      { k: "away", d: 2000 }, // empty deck beat before looping
    ] as const;
    const PERIOD = PHASES.reduce((s, p) => s + p.d, 0);

    // Ground-track fractions of the lane span at each phase boundary.
    const X = { touch: 0.08, rolled: 0.38, taxied: 0.54, liftoff: 0.98 };

    // Pitch pivot, in SVG user units (center of the fuselage).
    const PIVOT = "504 500";

    const easeIn = (p: number) => p * p;
    const easeOut = (p: number) => 1 - (1 - p) * (1 - p);
    const easeInOut = (p: number) => 0.5 - 0.5 * Math.cos(Math.PI * p);
    const lerp = (a: number, b: number, p: number) => a + (b - a) * p;

    const reduce = window.matchMedia("(prefers-reduced-motion: reduce)");

    // Rest pose: parked mid-deck, engines off.
    const placeStatic = () => {
      flyer.style.opacity = "1";
      flyer.style.transform = `translate3d(${(span / 2).toFixed(2)}px,0,0)`;
      plane.setAttribute("transform", `rotate(0 ${PIVOT})`);
      exhaust.setAttribute("opacity", "0");
    };

    // Travel range — kept fresh with a ResizeObserver so it survives the
    // initial layout settling and responsive width changes.
    let span = 0;
    let flyerW = 0;
    const measure = () => {
      flyerW = flyer.offsetWidth;
      span = Math.max(0, lane.clientWidth - flyerW);
      if (reduce.matches) placeStatic();
    };
    measure();
    const ro = new ResizeObserver(measure);
    ro.observe(lane);
    ro.observe(flyer);

    let raf = 0;
    let start = 0;
    let puffCooldown = 0;

    const firePuff = (xFrac: number, now: number) => {
      if (now < puffCooldown) return;
      puffCooldown = now + 900;
      puffEl.style.left = `${(xFrac * span + flyerW * 0.5).toFixed(0)}px`;
      puffEl.animate(
        [
          { opacity: 0.5, transform: "translate(0,0) scale(0.6)" },
          { opacity: 0, transform: "translate(-14px,-6px) scale(1.5)" },
        ],
        { duration: 700, easing: "ease-out" },
      );
    };

    const frame = (now: number) => {
      if (!start) start = now;
      measure(); // cheap read; self-heals across initial layout settling
      const t = (now - start) % PERIOD;

      let acc = 0;
      let idx = 0;
      let local = 0;
      for (let i = 0; i < PHASES.length; i++) {
        if (t < acc + PHASES[i].d) {
          idx = i;
          local = (t - acc) / PHASES[i].d;
          break;
        }
        acc += PHASES[i].d;
      }
      const ph = PHASES[idx].k;

      // x: fraction of span (goes <0 / >1 while off-screen)
      // y: px above the deck surface, pitch: deg (negative = nose up)
      let x = 0;
      let y = 0;
      let pitch = 0;
      let thrust = true;
      let visible = true;

      if (ph === "approach") {
        x = lerp(-0.28, X.touch, easeInOut(local));
        y = lerp(46, 4, easeOut(local));
        pitch = lerp(6, 2, local); // slight nose-down glide flattening out
      } else if (ph === "flare") {
        x = lerp(X.touch, X.touch + 0.09, local);
        y = lerp(4, 0, easeIn(local));
        // flare nose-up, then derotate onto the nose wheel
        pitch =
          lerp(2, -7, Math.min(1, local * 1.6)) +
          (local > 0.85 ? (local - 0.85) * 40 : 0);
        if (local > 0.9 && local < 0.98) firePuff(x, now);
      } else if (ph === "rollout") {
        x = lerp(X.touch + 0.09, X.rolled, easeOut(local));
        pitch = lerp(-1, 0, Math.min(1, local * 3));
      } else if (ph === "taxi") {
        x = lerp(X.rolled, X.taxied, easeInOut(local));
        thrust = false;
      } else if (ph === "roll") {
        x = lerp(X.taxied, X.liftoff, easeIn(local));
        pitch = local > 0.75 ? lerp(0, -9, (local - 0.75) / 0.25) : 0; // rotate
      } else if (ph === "climb") {
        x = lerp(X.liftoff, 1.3, local);
        y = lerp(0, 52, easeIn(local));
        pitch = -9;
      } else {
        visible = false;
      }

      flyer.style.opacity = visible ? "1" : "0";
      flyer.style.transform = `translate3d(${(x * span).toFixed(2)}px, ${(-y).toFixed(2)}px, 0)`;
      plane.setAttribute("transform", `rotate(${pitch.toFixed(2)} ${PIVOT})`);

      // Jet exhaust: shimmer under thrust, nearly gone at taxi idle.
      const flick = 0.6 + 0.4 * Math.abs(Math.sin(now / 55));
      exhaust.setAttribute("opacity", (thrust ? 0.55 * flick : 0.08).toFixed(3));

      raf = requestAnimationFrame(frame);
    };

    if (reduce.matches) {
      placeStatic();
      return () => ro.disconnect();
    }

    const onVisibility = () => {
      if (document.hidden) {
        cancelAnimationFrame(raf);
        raf = 0;
      } else if (!raf) {
        start = 0;
        raf = requestAnimationFrame(frame);
      }
    };
    document.addEventListener("visibilitychange", onVisibility);
    raf = requestAnimationFrame(frame);

    return () => {
      cancelAnimationFrame(raf);
      ro.disconnect();
      document.removeEventListener("visibilitychange", onVisibility);
    };
  });
</script>

